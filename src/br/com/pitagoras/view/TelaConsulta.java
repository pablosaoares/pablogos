    private void ListarValoresCandidatos() {
        try {
            CandidatoDAO candidatoDAO = new CandidatoDAO();

            DefaultTableModel model = (DefaultTableModel) TabelaCandidato.getModel();
            model.setNumRows(0);

            ArrayList<Candidato> lista = candidatoDAO.PesquisarCandidato();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNome(),
                    lista.get(num).getPartido(),
                    lista.get(num).getNumero(),
                    lista.get(num).getCargo()
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ListarValores " + erro);
        }

    }
    
    private void ListarValoresEleitores(){
        try {
            EleitorDAO eleitorDAO = new EleitorDAO();
            
            DefaultTableModel model = (DefaultTableModel) TabelaEleitores.getModel();
            model.setNumRows(0);
            
            ArrayList<Eleitor> lista = eleitorDAO.PesquisarEleitor();
            
            for (int num1 = 0; num1 < lista.size(); num1++){
                model.addRow(new Object[]{
                    lista.get(num1).getId(),
                    lista.get(num1).getNome(),
                    lista.get(num1).getTituloEleitor(),
                    lista.get(num1).getCpf()
                });
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ListarValores " + erro);
        }
    }

